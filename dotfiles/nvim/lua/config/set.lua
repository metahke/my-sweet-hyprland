vim.opt.guicursor = ""  -- fat cursor

-- show line numbers
vim.opt.nu = true
vim.opt.relativenumber = false  -- relative line nums, for example: 9j will go 9 lines up

vim.opt.tabstop = 4
vim.opt.softtabstop = 4
vim.opt.shiftwidth = 4
vim.opt.expandtab = true

vim.opt.smartindent = true

vim.opt.wrap = true

-- disable built in vim backup and make it possible to go days and days before with plugin undodir undotree?
vim.opt.swapfile = false
vim.opt.backup = false
vim.opt.undodir = os.getenv("HOME") .. "/.vim/undodir"
vim.opt.undofile = true

-- makes it possible to see what you are searching (highlights it)
vim.opt.hlsearch = false
vim.opt.incsearch = true

vim.opt.termguicolors = true

vim.opt.scrolloff = 8
vim.opt.signcolumn = "yes"
vim.opt.isfname:append("@-@")

vim.opt.updatetime = 50

vim.opt.colorcolumn = "80"

vim.g.mapleader = " "
